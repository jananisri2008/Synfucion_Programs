package numberprograms;

public class PerfectSquareNumber
{
	public static void main(String[] args) 
	{
		int num=10;
		int root=(int)Math.sqrt(num);
		if(num==root*root)
		{
			System.out.println("perfect square number");
		}
			else
			{
				System.out.println("not perfect square number");
				
			}
		}
}
